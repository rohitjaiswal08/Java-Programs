class IfElseProgram
{
    public static void main(String args[])
    {
        int num = 30;
  
        if (num > 25)
		{
            System.out.println(num + " is greater than 25");
		}
        else
		{
            System.out.println(num +" is less than 25");
		}
    }
}

// Output : 30 is greater than 25